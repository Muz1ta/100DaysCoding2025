public class indecrement {
    public static void main(String[] args){
        int a = 10;
        a++;
        System.out.println(a);
        --a;
        System.out.println(a);
        
        int b = a++;
        System.out.println("\n"+b);
        int c = --a;
        System.out.println(c);
        
        
    }  
}
